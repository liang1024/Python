foods = ["bacon", "tuna", "ham", "snausages", "beef"]

# 循环集合
for f in foods:
    print(f)

print("----------------------------")

# 循环集合,并打印元素的值
for f in foods:
    print(f)
    print(len(f))

print("----------------------------")

# 循环集合前两位，并打印元素的值
for f in foods[:2]:
    print(f)
    print(len(f))
